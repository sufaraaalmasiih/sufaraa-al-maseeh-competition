export type EasyBibleRow = string[];

const COL = 20;

function row(values: Partial<Record<number, string>>): EasyBibleRow {
  const r = Array.from({ length: COL }, () => "");
  for (const [index, value] of Object.entries(values)) {
    r[Number(index)] = value ?? "";
  }
  return r;
}

